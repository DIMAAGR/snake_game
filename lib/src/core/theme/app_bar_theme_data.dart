import 'package:flutter/material.dart';

class ApplicationAppBarTheme {
  //
  // ===================================================================
  // White appBar theme
  // ===================================================================
  static AppBarTheme get whiteAppBarTheme => const AppBarTheme(elevation: 0);

  // ===================================================================
  // Dark appBar theme
  // ===================================================================
  static AppBarTheme get darkAppBarTheme => const AppBarTheme(elevation: 0);

  // ===================================================================
  // Transparent appBar theme
  // ===================================================================
  static AppBarTheme get transparentAppBarTheme => const AppBarTheme(elevation: 0, color: Colors.transparent);
}
